/// Recover domain from TLS/HTTP for fake-ip + pure-IP destinations.
fn default_sniffer() -> serde_yaml::Value {
    let yaml = r#"
enable: true
force-dns-mapping: true
parse-pure-ip: true
override-destination: true
sniff:
  HTTP:
    ports: [80, 8080-8880]
    override-destination: true
  TLS:
    ports: [443, 8443]
  QUIC:
    ports: [443, 8443]
"#;
    serde_yaml::from_str(yaml).expect("sniffer template")
}

fn default_rules(paths: &Paths) -> Result<(Option<serde_yaml::Value>, serde_yaml::Value)> {
    // DustinWin ruleset — same set as user's work yaml
    let base = "https://github.com/DustinWin/ruleset_geodata/releases/download/mihomo-ruleset";
    let ruleset_dir = paths.run_dir().join("ruleset");
    fs::create_dir_all(&ruleset_dir)?;

    let entries: &[(&str, &str, &str, &str)] = &[
        ("private", "domain", "mrs", "private.mrs"),
        ("applications", "classical", "text", "applications.list"),
        ("bilibili", "domain", "mrs", "bilibili.mrs"),
        ("apple-cn", "domain", "mrs", "apple-cn.mrs"),
        ("microsoft-cn", "domain", "mrs", "microsoft-cn.mrs"),
        ("games-cn", "domain", "mrs", "games-cn.mrs"),
        ("cn", "domain", "mrs", "cn.mrs"),
        ("privateip", "ipcidr", "mrs", "privateip.mrs"),
        ("cnip", "ipcidr", "mrs", "cnip.mrs"),
    ];

    let mut rp = serde_yaml::Mapping::new();
    for (name, behavior, format, file) in entries {
        let mut m = serde_yaml::Mapping::new();
        m.insert("type".into(), "http".into());
        m.insert("behavior".into(), (*behavior).into());
        m.insert("format".into(), (*format).into());
        m.insert(
            "path".into(),
            ruleset_dir.join(file).to_string_lossy().to_string().into(),
        );
        m.insert("url".into(), format!("{base}/{file}").into());
        m.insert("interval".into(), 86400.into());
        m.insert("proxy".into(), "PROXY".into());
        rp.insert((*name).into(), serde_yaml::Value::Mapping(m));
    }

    let rules: Vec<serde_yaml::Value> = vec![
        "RULE-SET,private,DIRECT".into(),
        "RULE-SET,applications,DIRECT".into(),
        "RULE-SET,bilibili,DIRECT".into(),
        "RULE-SET,apple-cn,DIRECT".into(),
        "RULE-SET,microsoft-cn,DIRECT".into(),
        "RULE-SET,games-cn,DIRECT".into(),
        "RULE-SET,cn,DIRECT".into(),
        "RULE-SET,privateip,DIRECT,no-resolve".into(),
        "RULE-SET,cnip,DIRECT".into(),
        "MATCH,PROXY".into(),
    ];

    Ok((
        Some(serde_yaml::Value::Mapping(rp)),
        serde_yaml::Value::Sequence(rules),
    ))
}
